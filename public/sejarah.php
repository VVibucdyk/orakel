<h3>Ramalan</h3>
<p>
  Usaha-usaha untuk memperoleh pengetahuan atas pertanyaan atau
  situasi melalui cara-cara okultisme, ritual atau proses tertentu
</p>
<p>
  Ramalan pada dasarnya merupakan dugaan atau perkiraan mengenai
  terjadinya suatu kejadian atau peristiwa di waktu yang akan datang.
</p>
<p>
  Ramalan digunakan juga untuk mengetahui masa depan melalui cara-cara
  yang umumnya dipandang tidak rasional. Orang yang melakukan ramalan
  biasa disebut sebagai peramal, tukang/juru ramal, atau ahli nujum.
</p>
